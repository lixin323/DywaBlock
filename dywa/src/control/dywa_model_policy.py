#!/usr/bin/env python3
"""
Student model deployment interface.

Aligned with eval_student_unseen_obj.sh:
    ++student.norm="ln"
    ++student.decoder.film_mlp=1

Assets loaded from export_dir/ (generated by test_rma.py --export_cfg_dir):
    student.yaml, student.ckpt    — student model config & weights
    normalizer.yaml, normalize.ckpt — observation normalizer config & state
"""
from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Callable, Dict, Optional

import numpy as np
import torch as th
import yaml
from omegaconf import OmegaConf

from .se3 import SE3, assert_se3, step_towards


def _rot6d_from_R(R: np.ndarray) -> np.ndarray:
    """3x3 rotation matrix → 6D continuous representation (first two columns, row-major flatten)."""
    return np.asarray(R, dtype=np.float32).reshape(3, 3)[:, :2].reshape(6)


def _pose9d_from_se3(T: SE3) -> np.ndarray:
    """SE3 → [pos(3), rot6d(6)] = 9D."""
    assert_se3(T)
    pos = np.asarray(T[:3, 3], dtype=np.float32).reshape(3)
    rot6d = _rot6d_from_R(T[:3, :3])
    return np.concatenate([pos, rot6d]).astype(np.float32)


def _relative_pose9d_from_se3(goal_T: SE3, current_T: SE3) -> np.ndarray:
    """goal * inv(current) -> pose9d, aligned with training rel_goal_gt."""
    assert_se3(goal_T)
    assert_se3(current_T)
    T_rel = np.asarray(goal_T, dtype=np.float32) @ np.linalg.inv(np.asarray(current_T, dtype=np.float32))
    assert_se3(T_rel)
    return _pose9d_from_se3(T_rel)


# ---------------------------------------------------------------------------
#  Lightweight normalizer for deployment (no IsaacGym / gym dependency)
# ---------------------------------------------------------------------------

class _DeployNormalizer:
    """Reproduces EnvNormalizer.normalize_obs logic using exported yaml+ckpt."""

    def __init__(self, yaml_path: Path, ckpt_path: Path, device: str):
        with open(yaml_path) as f:
            cfg = yaml.safe_load(f)
        self.device = th.device(device)
        self.eps = float(cfg.get("obs_eps", 1e-6))

        constlist = set(cfg.get("constlist", []))
        stats = cfg.get("stats", {}) or {}

        self._const: Dict[str, tuple] = {}
        for k in constlist:
            s = stats.get(k)
            if s is not None and len(s) == 2:
                mean = th.tensor(s[0], dtype=th.float32, device=self.device)
                std = th.tensor(s[1], dtype=th.float32, device=self.device)
                self._const[k] = (mean, std)

        self._running: Dict[str, tuple] = {}
        sd = th.load(str(ckpt_path), map_location="cpu").get("normalizer", {})
        for key in list(sd.keys()):
            if key.startswith("obs_rms.") and key.endswith(".mean"):
                obs_key = key[len("obs_rms."):-len(".mean")]
                var_key = f"obs_rms.{obs_key}.var"
                if obs_key not in constlist and var_key in sd:
                    self._running[obs_key] = (
                        sd[key].to(self.device),
                        sd[var_key].to(self.device),
                    )

    def normalize(self, obs: Dict[str, th.Tensor]) -> Dict[str, th.Tensor]:
        """Normalize an obs dict in-place-safe manner."""
        out = {}
        for k, v in obs.items():
            if k in self._const:
                mean, std = self._const[k]
                out[k] = (v - mean) * th.reciprocal(std + self.eps)
            elif k in self._running:
                mean, var = self._running[k]
                out[k] = (v - mean) * th.reciprocal(th.sqrt(var) + self.eps)
            else:
                out[k] = v
        return out

    def normalize_cloud(self, ref_key: str, cloud: th.Tensor) -> th.Tensor:
        """Normalize a point cloud using the same constant params as *ref_key*."""
        if ref_key in self._const:
            mean, std = self._const[ref_key]
            return (cloud - mean) * th.reciprocal(std + self.eps)
        return cloud


